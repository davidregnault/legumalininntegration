                <h3 class="article_titre">Salon de l’agriculture 2018, le parcours du Bio </h3>
                <p class="article_texte">Alors que le bio continue de séduire les producteurs français, le Salon International de l’agriculture 2018 propose un "parcours bio" pour guider ses visiteurs vers les stands proposant les produits et animations liés au label.
                </p>
                <p class="article_texte">Chargée du développement de l’agriculture biologique française, l’Agence Bio anime un stand cette année au salon de l’agriculture (B010 – pavillon 4).
                    Interrogé le 23 février sur franceinfo, Florent Guhl, directeur de l’Agence Bio, a communiqué la mise en place d’un “parcours bio”. </p>
                <p class="article_texte">Son objectif ? Guider les visiteurs dans les allées du salon, balisant tous les stands liés au label bio via un « social wall ».
                    Un écran tactile disposé sur le stand de l’Agence Bio référence les publications Facebook, Twitter et Instagram qui contiennent le hashtag #SIAbio.
                    Les visiteurs et les acteurs de l’agriculture biologique présents sur le SIA (Salon International de l’Agriculture) sont invités à utiliser ce hashtag #SIAbio pour enrichir ce parcours et entretenir la conversation sur les réseaux sociaux durant les 9 jours de salon.
                </p>
                <p class="article_texte">Les utilisateurs pourront donc naviguer sur l’écran tactile installé sur le stand de l’Agence Bio et faire leur recherche par filière pour trouver les animations liées au bio.
                </p>
                <p class="article_texte">Vous avez jusqu’au 4 mars 2018 pour profiter des animations du Salon de l’Agriculture et vivre l’expérience “grandeur nature”!
                </p>
                <p class="article_texte">Pour en savoir plus, rendez vous sur le <a class="article_source" href=" http://www.bioalaune.com/fr/actualite-bio/36136/salon-agriculture-parcours-bio-reperer-animations-produits-bio">site de l'évènement !</a></p>
