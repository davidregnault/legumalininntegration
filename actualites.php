                <h3 class="article_titre">La ferme du Bec Hellouin</h3>
                <p class="article_texte">Modèle de permaculture mondiale, cette ferme utilise l'ensemble de la nature pour produire.
                </p>
                <p class="article_texte">Cette ferme française de 1 hectare s’inspire du fonctionnement de la nature pour créer un cercle vertueux. Perrine et Charles Hervé-Gruyer utilisent la permaculture. Cette technique d’origine Australienne permet d’imiter la nature dans le domaine de l’agriculture. Ici, chaque être vivant, d’origine animale ou végétale, a sa place, et surtout, à son rôle. Les organismes vivants travaillent à la place des machines,
                    l’objectif étant de faire en sorte que les déchets de l’un soit les produits de l’autre. </p>
                <p class="article_texte">C’est la mise en place d’un système économiquement viable, environnementalement durable et socialement équitable. La terre s’enrichit naturellement avec le développement des micro-organismes. La ferme du Bec Hellouin est composé de différents écosystèmes qui travaillent ensemble. On retrouve ainsi de nombreux arbres fruitiers, des mares, et quelques animaux.
                    L’ensemble des productions est destiné aux restaurants et magasins bios locaux, et également aux particuliers.</p>
                <p class="article_texte">N’oubliez pas que pour limiter les pesticides, le plus important est de privilégier des produits issus de l’agriculture biologique et cultivés en France.
                </p>
